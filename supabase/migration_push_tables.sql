--   - information_schema.columns  (الأعمدة، الأنواع، الـ defaults)
--   - pg_constraint                (PK / UNIQUE / FK بالتفصيل)
--   - pg_class.relrowsecurity      (هل RLS مفعّل)
--   - pg_policies                  (الـ policies الفعلية)
--   - pg_publication_tables        (هل realtime مفعّل)
--
-- الملف آمن للتشغيل على DB فيه الجدولين دول بالفعل (كل شيء IDEMPOTENT عبر
